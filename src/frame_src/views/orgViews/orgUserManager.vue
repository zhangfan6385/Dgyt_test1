<template>
 <div class="app-container calendar-list-container"> 
    <imp-panel>
   
 <el-row slot="body" :gutter="24" style="margin-bottom: 20px;">
     <el-col :span="6" :xs="24" :sm="24" :md="6" :lg="6" style="margin-bottom: 20px;">
        <el-tree v-if="roleTree"
                 :data="roleTree"
                 ref="roleTree" 
                 highlight-current
                 :render-content="renderContent"
                 @node-click="handleNodeClick" clearable node-key="id" :props="defaultProps"></el-tree>
      </el-col>
<el-col :span="18" :xs="24" :sm="24" :md="18" :lg="18">
    <el-card class="box-card">
   
    <div class="filter-container">
       <el-input @keyup.enter.native="handleFilter" style="width: 200px;" class="filter-item" :placeholder="$t('userTable.userName')" v-model="listQuery.userName">
      </el-input>
      <el-select clearable style="width: 120px" class="filter-item" v-model="listQuery.flag" :placeholder="$t('userTable.flag')">
        <el-option v-for="item in flagOptions" :key="item.key" :label="item.flag_name" :value="item.key">
        </el-option>
      </el-select>
       <el-select @change='handleFilter' style="width: 120px" class="filter-item" v-model="listQuery.sort">
        <el-option v-for="item in sortOptions" :key="item.key" :label="item.label" :value="item.key">
        </el-option>
      </el-select>
      <el-button class="filter-item" type="primary" v-waves icon="el-icon-search" @click="handleFilter">{{$t('userTable.search')}}</el-button>
      <el-button class="filter-item"  @click="updateRole" type="primary" icon="el-icon-edit">{{$t('roleTable.mount')}}</el-button>
     </div>
      <el-table :key='tableKey' :data="list" :header-cell-class-name="tableRowClassName"   @selection-change="handleSelectionChange" v-loading="listLoading" element-loading-text="给我一点时间" border fit highlight-current-row
      style="width: 100%">
   
         <el-table-column 
          prop="id"
          type="selection"
          width="50"> 
        </el-table-column>


      <el-table-column width="110px" align="center" :label="$t('userTable.userCode')">
        <template slot-scope="scope">
          <span>{{scope.row.userCode}}</span>
        </template>
      </el-table-column>
      <el-table-column width="110px" align="center" :label="$t('userTable.userName')">
        <template slot-scope="scope">
          <span>{{scope.row.userName}}</span>
        </template>
      </el-table-column>
      <el-table-column width="110px" align="center" :label="$t('userTable.flag')">
        <template slot-scope="scope">
          <el-tag>{{scope.row.flag | typeFilter}}</el-tag>
        </template>
       </el-table-column>
      <el-table-column min-width="180px"  align="center" :label="$t('userTable.remark')">
        <template slot-scope="scope">
          <span>{{scope.row.remark}}</span>
        </template>
       </el-table-column>
       <el-table-column min-width="110px"  align="center" :label="$t('userTable.orgName')">
        <template slot-scope="scope">
          <span>{{scope.row.orgName}}</span>
        </template>
       </el-table-column>
    </el-table>
      <div class="pagination-container">
      <el-pagination background @size-change="handleSizeChange" @current-change="handleCurrentChange" :current-page="listQuery.page" :page-sizes="[10,20,30, 50]" :page-size="listQuery.limit" layout="total, sizes, prev, pager, next, jumper" :total="total">
      </el-pagination>
    </div> 
   
      </el-card>
      </el-col>
    </el-row>
    </imp-panel>
  </div>
</template>
<script>
import { fetchOrgList, updateUserOrgArticle } from '@/frame_src/api/org'
import { fetchUserOrgList } from '@/frame_src/api/user'
import waves from '@/frame_src/directive/waves' // 水波纹指令
// import { parseTime } from '@/frame_src/utils'
import panel from '@/frame_src/components/TreeList/panel.vue'
import treeter from '@/frame_src/components/TreeList/treeter'
const flagOptions = [{ key: 0, flag_name: '否' }, { key: 1, flag_name: '是' }]
// arr to obj ,such as { CN : "China", US : "USA" }
const flagOptionsKeyValue = flagOptions.reduce((acc, cur) => { // 把cur的key值做成数组id，值是名称
  acc[cur.key] = cur.flag_name
  return acc
}, {})

export default {
  mixins: [treeter], // 把对象混入，其实就是可以调用到树里的数据，树嵌入到当前页面
  components: {
    'imp-panel': panel
  },
  name: 'userManager',
  directives: {
    waves
  },
  data() {
    return {
      tableKey: 0,
      list: null,
      total: null,
      listLoading: true,
      showUserPass: false,
      listQuery: {
        page: 1,
        limit: 5,
        userName: undefined,
        flag: undefined,
        importance: undefined,
        sort: '+userId',
        orgId: undefined
      },
      flagOptions,
      sortOptions: [
        { label: '正序', key: '+userId' },
        { label: '倒序', key: '-userId' }
      ],
      multipleSelection: [],
      temp: {
        userId: undefined,
        userCode: '',
        userName: '',
        userAlias: '',
        userPass: '',
        phoneMobile: '',
        phoneOffice: '',
        phoneOrg: '',
        userEmail: '',
        emailOffice: '',
        userIp: '',
        flag: '',
        userDomain: '',
        remark: '',
        orgName: '',
        orgId: ''

      }, listUpdate: {

        orgId: undefined,
        multipleSelection: []
      },
      roleTree: [],
      defaultProps: {
        children: 'children',
        label: 'orgName',
        id: 'id'
      }, treeListQuery: {
        sysCode: undefined
      }
    }
  },
  filters: { // 本地过滤器
    typeFilter(type) { // 调用方法把下拉数据进行重新拼装，并获取到名称的值
      return flagOptionsKeyValue[type]
    }
  }, watch: { // 监听器，当multipleSelection 发生改变时
    multipleSelection: function() { // 把选中的数据id放到数组里，以便后期传值用
      const arr = []
      for (const i in this.multipleSelection) {
        arr.push(this.multipleSelection[i].userId)
      }
    }
  },
  methods: {
    getList() { // 查询组织结构对应的用户数据
      this.listLoading = true
      fetchUserOrgList(this.listQuery).then(response => {
        this.list = response.data.items
        this.total = response.data.total
        this.listLoading = false
      })
    }, load() { // 查询组织结构数据
      this.treeListQuery.sysCode = '2'
      fetchOrgList(this.treeListQuery).then(response => {
        this.roleTree = response.data.items
      })
      // this.roleTree.push(...defaultValue.roleList);
    },
    handleSizeChange(val) {
      this.listQuery.limit = val
      this.getList()
    },
    handleCurrentChange(val) {
      this.listQuery.page = val
      this.getList()
    },
    handleFilter() {
      this.listQuery.page = 1
      this.getList()
    }, renderContent(h, { node, data, store }) { // 给左边树进行遍历
      return (
        <span>
          <span>
            <span>{node.label}</span>
          </span>
        </span>)
    }, handleNodeClick(data) { // 点击左侧树查询对应的用户信息
      this.listLoading = true
      this.listQuery.orgId = this.$refs.roleTree.getCurrentKey()

      fetchUserOrgList(this.listQuery).then(response => {
        this.list = response.data.items
        this.total = response.data.total
        this.listLoading = false
      })
    }, updateRole() { // 修改组织结构权限
      if (this.multipleSelection.length <= 0 || this.$refs.roleTree.getCurrentKey() == null) {
        this.$notify({// 判断右边记录的勾选数据 和左侧树选中的key值是否为0或者空
          title: '失败',
          message: '请选择角色和用户',
          type: 'error',
          duration: 2000
        })
      } else {
        this.listUpdate.orgId = this.$refs.roleTree.getCurrentKey() // 左侧树的key值
        this.listUpdate.multipleSelection = this.multipleSelection // 右边选中的集合
        updateUserOrgArticle(this.listUpdate).then(response => { // 给用户分配组织结构权限
          var message = response.data.message
          var title = '失败'
          var type = 'error'
          if (response.data.result === true) {
            title = '成功'
            type = 'success'
            this.getList()
            this.load()
          }
          this.$notify({
            title: title,
            message: message,
            type: type,
            duration: 2000
          })
        })
      }
    }, handleSelectionChange(val) { // 勾选右边表格时记录勾选的数据
      this.multipleSelection = val
    }, tableRowClassName({ row, rowIndex }) { // 通过右边表格的class名 挂载css样式
      if (rowIndex === 0) {
        return 'el-button--primary is-active'// 'warning-row'
      } // 'el-button--primary is-plain'// 'warning-row'
      return ''
    }
  },
  created() { // 实例已经创建完成
    this.getList()
    this.load()
  }
}
</script>
